package LanguageFundamentals.VariablesAndDatatypes;

public class DatatypeRange {
    public static void main(String args[]) {
        byte maxval = Byte.MAX_VALUE;
        byte minval = Byte.MIN_VALUE;

        int maxval2 = Integer.MAX_VALUE;
        int minval2 = Integer.MIN_VALUE;

        System.out.println("Byte Minimum value " + minval + ", Maximum value " + maxval);
        System.out.println("Integer Minimum value " + minval2 + ", Maximum value " + maxval2);

    }
}

