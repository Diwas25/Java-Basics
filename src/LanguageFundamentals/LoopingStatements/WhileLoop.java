package LanguageFundamentals.LoopingStatements;

public class WhileLoop {
    public static void main(String args[])
    {
        int a=1;
        System.out.println("Start of Loop !!");
        while(a<=10){
            System.out.println("Value = "+a);
            a++;
        }
        System.out.println("End of Loop !!");
    }
}
